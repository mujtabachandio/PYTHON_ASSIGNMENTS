                                                                        # Python Operators

# Arithmetic Operators
a = 10
b = 3
print('Addition:', a + b)
print('Subtraction:', a - b)
print('Multiplication:', a * b)
print('Division:', a / b)
print('Floor Division:', a // b)
print('Modulus:', a % b)
print('Exponentiation:', a ** b)

# Assignment Operators
x = 5
x += 3  # x = x + 3
print('Assignment:', x)

# Comparison Operators
print('Equal:', a == b)
print('Not Equal:', a != b)
print('Greater Than:', a > b)
print('Less Than:', a < b)
print('Greater or Equal:', a >= b)
print('Less or Equal:', a <= b)

# Logical Operators
c = True
d = False
print('AND:', c and d)
print('OR:', c or d)
print('NOT:', not c)

# Identity Operators
e = [1, 2, 3]
f = [1, 2, 3]
g = e
print('Is:', e is g)
print('Is Not:', e is not f)

# Membership Operators
print('In:', 2 in e)
print('Not In:', 4 not in e)

# Bitwise Operators
i = 6  # 110 in binary
j = 2  # 010 in binary
print('Bitwise AND:', i & j)
print('Bitwise OR:', i | j)
print('Bitwise XOR:', i ^ j)
print('Bitwise NOT:', ~i)
print('Left Shift:', i << 1)
print('Right Shift:', i >> 1)
